using UnityEngine;
using Utilities;

using System;
using System.Collections.Generic;
using System.Linq;

namespace AI {

    [Serializable]
    public class AICooldown {
        public string Name;
        public int ID;
        public CountDownTimer Timer;
        public bool Managed = true;

        public AICooldown(string name, float cooldown, bool managed = true) {
            Name = name;
            ID = AICooldownManager.GetHash(name);
            Timer = new CountDownTimer(cooldown);
            Managed = managed;
        }

        public AICooldown(string name, int id, float cooldown, bool managed = true) {
            Name = name;
            ID = id;
            Timer = new CountDownTimer(cooldown);
            Managed = managed;
        }
    }

    ///<summary>Manager for cooldowns used by states - uses CountDownTimer as timer implementation</summary>
    [Serializable]
    public class AICooldownManager {
        [SerializeField] private List<AICooldown> _cooldowns;
        private readonly Dictionary<int, int> _cooldownIDs;

        public AICooldownManager() {
            _cooldowns = new List<AICooldown>();
            _cooldownIDs = new Dictionary<int, int>();
        }

        public AICooldownManager(AICooldown[] cooldowns) {
            _cooldowns = cooldowns.ToList();
            _cooldownIDs = new Dictionary<int, int>();
            for (int i = 0; i < _cooldowns.Count; i++) {
                _cooldownIDs.Add(_cooldowns[i].ID, i);
            }
        }

        ///<summary>Computes reliable hash from string</summary>
        ///<param name="name">Value to hash</param>
        ///<returns>Hash of string <c>name</c></returns>
        public static int GetHash(string name) {
            return Animator.StringToHash(name);
        }

        ///<summary>Updates all managed cooldowns</summary>
        ///<param name="dt">Amount to tick managed cooldowns</param>
        public void Update(float dt) {
            foreach (AICooldown cooldown in _cooldowns) {
                if (cooldown.Managed) {
                    cooldown.Timer.Update(dt);
                }
            }
        }

        ///<summary>Creates a new cooldown from params</summary>
        ///<param name="time">Cooldown duration</param>
        ///<param name="name">Cooldown name</param>
        ///<param name="managed">Is cooldown managed by Update()</param>
        ///<returns>ID of created cooldown</returns>
        public int CreateCooldown(float time, string name, bool managed = false) {
            if (name == string.Empty) { name = $"[{_cooldowns.Count}] Cooldown"; }
            int id = GetHash(name);

            if (_cooldownIDs.ContainsKey(id)) {
                return id;
            }

            _cooldowns.Add(new AICooldown(name, id, time, managed));
            _cooldownIDs.Add(id, _cooldowns.Count - 1);

            return id;
        }

        ///<summary>Creates a new cooldown from id</summary>
        ///<remarks>Name will be in format Autogenered - {id}</remarks>
        ///<param name="time">Cooldown duration</param>
        ///<param name="id">Cooldown id</param>
        ///<param name="managed">Is cooldown managed by Update()</param>
        public void CreateCooldown(float time, int id, bool managed = false) {
            _cooldowns.Add(new AICooldown($"Autogenerated - {id}", id, time, managed));
            _cooldownIDs.Add(id, _cooldowns.Count - 1);
        }

        ///<summary>Gets a cooldown's internal timer</summary>
        ///<remarks>Will generate cooldown if not present</remarks>
        ///<param name="id">Id of cooldown - usually corresponds to GetHash(cooldownName)</param>
        ///<returns>Cooldown timer (non null)</returns>
        public CountDownTimer Get(int id) {
            if (_cooldownIDs.TryGetValue(id, out int index)) {
                return _cooldowns[index].Timer;
            } else {
                CreateCooldown(0.0f, id, false);
                return _cooldowns[_cooldownIDs[id]].Timer;
            }
        }

        ///<summary>Returns if cooldown timer is finished</summary>
        ///<remarks>Will generate cooldown if not present</remarks>
        ///<param name="id">Id of cooldown - usually corresponds to GetHash(cooldownName)</param>
        ///<returns>IsFinished value of internal cooldown timer</returns>
        public bool IsFinished(int id) {
            return Get(id).IsFinished;
        }
    }
}